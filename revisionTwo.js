//string concat , string interpolation

let firstName = "mayuri"
let lastName = "kalegore"
console.log("My firstName is " + firstName + " and lastName is " + lastName)  //string concat   
console.log(`My firstName is ${firstName} and lastName is ${lastName}`)  //string interpolation 

console.log(`${1 + 1}`) //string
console.log(1+1) //number